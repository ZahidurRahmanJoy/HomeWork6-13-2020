package project4;

public class IfElse {
	
	
	public static void main(String[] args) {    	
        int mobilePrice = 100000;

        // checks if mobilePrice is greater than 100000	 
        if (mobilePrice > 10000) {
            System.out.println("This Phone is Expensive!");
        }
        else {
            System.out.println("This phone is CHEAP!");
        }
 
        System.out.println("//That's how you write 'if.....else' Example");
    }


}
